package com.example.junit5.sujan;

public class Apple {
    private int appleId;
    private String appleName;

    public Apple(int appleId, String appleName) {
        this.appleId = appleId;
        this.appleName = appleName;
    }

    public int getAppleId() {
        return appleId;
    }

    public String getAppleName() {
        return appleName;
    }
}
